/// <summary>
/// Represents types of stat distributions available when generating a character
/// Balanced:  All stats evenly distributed
/// EMP: Favors Empathy and Cool
/// INT: Favors intelligence and tech
/// REF: Favors reflex and body
/// </summary>
public enum BuildType { Balanced, EMP, INT, REF, Silly }